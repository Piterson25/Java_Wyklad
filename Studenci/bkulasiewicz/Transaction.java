import java.time.LocalDate;
import java.util.Objects;

public class Transaction {
    private  Account source;
    private Account destination;
    private String title;
    private  double amount;
    private LocalDate date;
    private static int idCounter = 0;
    private int id;

    public Transaction(Account source,Account destination,String title, double amount) {
        this.source=source;
        this.destination=destination;
        this.title=title;
        this.amount=amount;
        this.date=LocalDate.now();
        this.id=idCounter;
        idCounter++;

    }

    @Override
    public String toString() {
        return "Transaction{" + "source=" + source + "destination=" +
                destination + "title=" + title + "amount=" + amount + "date="
                + date + "id=" + id + "}";
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;
        Transaction that = (Transaction) obj;
        return Double.compare(that.amount, amount) == 0 &&
                id == that.id &&
                Objects.equals(source, that.source) &&
                Objects.equals(destination, that.destination) &&
                Objects.equals(title, that.title) &&
                Objects.equals(date, that.date);

    }

    @Override
    public int hashCode() {
        return Objects.hash(source,destination,title,amount,date,id);

    }
    public Account getSource(){
        return source;
    }

    public Account getDestination() {
        return destination;
    }

    public String getTitle() {
        return title;
    }

    public double getAmount() {
        return amount;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }

}
